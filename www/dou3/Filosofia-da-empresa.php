<p class="subtitulo">Revisão e Alinhamento de Cultura Organizacional – A Cultura Adequada</p>
<p class="corpotexto">Cultura é o “sistema operacional” de uma organização. É a influência recíproca, viva e dinâmica de todas as crenças, valores, normas e práticas compartilhados, que atuam consciente e inconscientemente dentro de uma organização. A cultura guia o que as pessoas consideram importante e como elas pensam, agem, sentem e trabalham.</p>
<p class="corpotexto">Certos aspectos da cultura não são diretamente perceptíveis, mas orientam e estão refletidos nas ações realizadas por uma organização. A menos que tais crenças e atitudes venham à tona e sejam devidamente trabalhadas, elas podem permitir que pessoas bem-intencionadas minem, sem querer, a continuidade do sucesso da organização. Reações instintivas e por reflexo, a eventos e desafios, podem até mesmo sabotar o progresso organizacional. “Atitudes culturais” podem minar os esforços necessários para mudanças, levando à falsa conclusão de que a mudança é impossível, de tão difícil.</p>
<p class="corpotexto">Ter sucesso significa ter uma cultura forte e integrada, que apóia sua visão, sua estratégia e seus objetivos. Uma organização com Cultura Adequada é uma organização que:</p>
<ul>
<li class="corpotexto">Inova e lidera mercados. </li>
<li class="corpotexto">Conhece e atende seus clientes. </li>
<li class="corpotexto">Atrai e mantém os funcionários mais talentosos. </li>
<li class="corpotexto">Proporciona sólidos retornos a seus acionistas. </li>
<li class="corpotexto">Tem um propósito orientador e vive seus valores para despertar o que as pessoas têm de melhor a oferecer, inspirando a lealdade e o compromisso delas. </li></ul>
<p class="corpotexto">A construção da Cultura Adequada consiste em identificar a cultura existente, esclarecer objetivos e estratégias de negócios, definir a cultura desejada, para então projetar as iniciativas de mudança para eliminar as falhas. </p>
<p><span class="corpotexto">Cultura é reconhecida como sendo importante em uma organização, mas ao mesmo tempo é considerada como sendo complexa e difícil de medir. A pesquisa de opinião dos funcionários permite que a organização avalie objetivamente seu contexto interno num grau mais profundo. Esta “foto-instantânea” da cultura organizacional pode ajudar a avaliar se a atual cultura apóia e dirige as ações necessárias para alcançar as metas da organização. A compreensão desses resultados também pode ser utilizada para informar e orientar oportunidades de fusões e aquisições, reorganizações, contratações e decisões sobre sucessão, bem como estratégias de crescimento e renovação. </span><br />
</p>
<p align="right"><a href="http://www.directionrh.com.br/desevorganiz5.htm#topo"></a></p><strong><a id="2" name="2"></a></strong>
<hr />
<p class="corpotexto"><span class="subtitulocinza">Por Que Avaliar a Cultura?</span></p>
<p class="corpotexto">Cultura organizacional não é somente uma lista de valores desenvolvidos por um grupo. Estes são “ideais”. O que você se esforça para ser como organização e aquilo que você pensa que apóia podem ser diferentes das crenças e valores que estão, na verdade, sendo demonstrados. É fundamental que se descubra quem realmente é, antes de se decidir quem quer ser.</p>
<p class="corpotexto">Olhar uma organização pelo lado de dentro pode não lhe dar uma visão precisa e objetiva sobre ela. A alta direção comumente tem uma visão da organização bem diferente da que seus funcionários têm e é difícil dirigir uma organização com uma visão que pode ser idealizada e distorcida de sua natureza.</p>
<p class="corpotexto">Identificando as discrepâncias entre a cultura existente e a cultura ideal, podemos personalizar, juntamente com o cliente, um programa de mudanças com foco naquela organização e voltado para resultados. Trabalhamos com a liderança da organização para identificar as ações de sucesso rápido, as simbólicas e as ações substantivas necessárias para criar a Cultura Adequada. </p>
<p class="corpotexto">A mudança de cultura é freqüentemente considerada como sendo difícil, mesmo quando há necessidade clara e urgente de mudar. Quando algo vem obtendo sucesso, existe uma resistência natural em mudar. A pesquisa de opinião dos funcionários auxilia a organização a definir o que ainda a ajuda a obter sucesso e objetivamente informa sobre aspectos da cultura que possam estar atrapalhando seu sucesso ou aumentando o risco de fracasso no futuro.</p>
<p align="right"><a href="http://www.directionrh.com.br/desevorganiz5.htm#topo"></a></p><strong><a id="3" name="3"></a></strong>
<hr />
<p class="corpotexto"><span class="subtitulocinza">Como se Molda e Transmite a Cultura?</span></p>
<p class="corpotexto">A base da cultura de uma organização são as premissas, valores e procedimentos operacionais de seus fundadores, as qualidades e características das pessoas que trabalham na organização e as respostas da organização a eventos e desafios.</p>
<p class="corpotexto">A cultura é perpetuada e transmitida por:</p>
<ul>
<li class="corpotexto">Declarações formais de filosofia e valores da organização. </li>
<li class="corpotexto">Seu desempenho, sistemas de promoção, desenho e estrutura da organização. </li>
<li class="corpotexto">Seus sistemas e procedimentos (como as coisas são feitas). </li>
<li class="corpotexto">Seus critérios de recrutamento, seleção e desligamento. </li></ul>
<p class="corpotexto">Os líderes desempenham um papel crucial no desenvolvimento da cultura. Eles formam a cultura pelo seu modelo de comportamento, pelas coisas em que prestam atenção, pelo que medem e controlam e por suas reações a incidentes críticos e crises que ameaçam a sobrevivência e põem à prova os valores da organização. </p>
<p class="corpotexto">Cultura também se transmite pela história informal da organização, que é compartilhada pelas histórias e lendas sobre as pessoas mais importantes e acontecimentos que afetaram a organização.</p>
<p class="corpotexto">O desafio de cada organização é analisar sua cultura atual e modificá-la constantemente, em resposta aos desafios internos e externos. Os elementos de uma cultura, que podem ter sido eficazes e essenciais em certa ocasião, podem ter-se tornado os próprios elementos culturais ineficazes e desfavoráveis em outra.</p>
<p class="corpotexto">Organizações de sucesso estão sempre reagindo à constante mudança nos eventos de negócios que exigem adaptação e impulsionam as modificações culturais. Alguns deles são:</p>
<ul>
<li class="corpotexto">Atrair e reter talentos. </li>
<li class="corpotexto">Fusões, aquisições, joint ventures e alianças. </li>
<li class="corpotexto">Novas tecnologias, produtos ou serviços. </li>
<li class="corpotexto">Rotatividade de executivos. </li>
<li class="corpotexto">Planejamento da sucessão. </li>
<li class="corpotexto">Renovação organizacional. </li>
<li class="corpotexto">Administração do crescimento. </li>
<li class="corpotexto">Globalização. </li></ul>
