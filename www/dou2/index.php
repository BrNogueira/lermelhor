<p align="center"><font color="#808040" size="4"><u><strong>&quot;TEORIA DO DESENVOLVIMENTO ORGANIZACIONAL&quot;</strong></u></font><span><font size="2">&nbsp;</font></span></p>
<p align="left"><em><font size="2">Prof. Ilacyr Luiz Gualazzi (extraído da página: <strong><font color="#0000ff">http://www.unimep.br/~gualazzi</font></strong>)</font></em></p>
<p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; A Teoria do Desenvolvimento Organizacional (DO) surgiu a partir de 1962, decorrente das idéias de vários autores, a respeito do ser humano, da organização e do ambiente em que estes crescem e se desenvolvem.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Assim, o conceito de Desenvolvimento Organizacional está intimamente ligado aos conceitos de mudança e de capacidade adaptativa à mudança</p><strong>
<p align="justify">CONCEITO DE ORGANIZAÇÃO<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong>O conceito de organização para os especialistas em D O é tipicamente comportamentalista: &quot;uma organização é a coordenação de diferentes atividades de contribuintes individuais com a finalidade de efetuar transações planejadas com o ambiente&quot;. Esse conceito utiliza a noção tradicional de divisão do trabalho ao se referir às diferentes atividades e à coordenação existente na organização e refere-se às pessoas como contribuintes das organizações, em vez de estarem elas próprias, as pessoas, totalmente nas organizações. As contribuições de cada participante à organização variam enormemente em função não somente das diferenças individuais, mas também do sistema de recompensas e contribuições pela organização.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Toda organização atua em determinado meio ambiente e sua existência e sobrevivência dependem da maneira como ela se relaciona com esse meio. Assim, ela deve ser estruturada e dinamizada em função das condições e circunstâncias que caracterizam o meio em que ela opera.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Os autores do DO adotam uma posição antagônica ao conceito tradicional da organização, salientando as diferenças fundamentais existentes entre os sistemas mecânicos (típicos do conceito tradicional) e os sistemas orgânicos (abordagem do DO).Os sistemas orgânicos tornam as organizações coletivamente conscientes dos seus destinos e da orientação necessária para melhor se dirigir a eles. Desenvolvem uma nova conscientização social dos participantes das organizações, os quais, contando com sua vivência particular, seu passado pessoal e sua autoconscientização, definem o papel deles em relação à sua organização.
